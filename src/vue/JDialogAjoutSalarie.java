    private void jButtonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValiderActionPerformed
        try{
            String requete = "insert into salarie (Code,Nom,Prenom,DateNaiss,DateEmbauche,Fonction,TauxHoraire,situationFamiliale,NbrEnfants,NumCat,CodeServ) values (?,?,?,?,?,?,?,?,?,?,?)";
            cnx = ConnexionBDD.getConnexion();
            pstmt = cnx.prepareStatement(requete);

            pstmt.setString(1,jTextFieldCode.getText());
            pstmt.setString(2,jTextFieldNom.getText());
            pstmt.setString(3,jTextFieldPrenom.getText());

            pstmt.setDate(4,convertUtilToSql(jDateChooser1.getDate()));

            pstmt.setDate(5,convertUtilToSql(jDateChooser2.getDate()));

            pstmt.setString(6,jTextFieldFonction.getText());
            pstmt.setString(7,jTextFieldTH.getText());
            pstmt.setString(8,jTextFieldFamille.getText());
            pstmt.setString(9,jTextFieldEnfant.getText());
            pstmt.setString(10, (String) jComboBoxCat.getSelectedItem());
            pstmt.setString(11,jTextFieldService.getText());

            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(null, "saved");

        }catch(Exception e){
            System.out.println("SQLException: "+e);
        }finally{
            try{
                pstmt.close();
                rs.close();
            }catch(Exception e){

            }
        }

    }//GEN-LAST:event_jButtonValiderActionPerformed

